package calculator;

public class Calculator {
    static int add(int n1, int n2) {
        return n1 + n2;
    }
    static int sub(int n1, int n2) {
        return n1 - n2;
    }
    static int multi(int n1, int n2) {
        return n1 * n2;
    }
    static double div(double n1, double n2) {
        return n1 / n2;
    }
}
